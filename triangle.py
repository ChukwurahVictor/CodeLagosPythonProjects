#Print the purpose of the program
print("This program calculates the area of a triangle")

#Prompt the user for the base of the triangle
base = int(input("Enter the value of the base: "))

#Prompt the user for the height of the triangle
height = int(input("Enter the value of the height: "))

#Calculate the area of the triangle
area = (1 / 2) * base * height

#Print the area of the triangle
print ("The area of the triangle is", area)
